package controller;

public class LoginVO {
	
	public int pkLogin;
	
	public String mail;
	
	public String pass;

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

	public int getPkLogin() {
		return pkLogin;
	}

	public void setPkLogin(int pkLogin) {
		this.pkLogin = pkLogin;
	}
	
	

}
